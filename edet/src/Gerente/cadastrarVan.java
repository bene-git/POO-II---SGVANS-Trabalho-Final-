    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String placa = letras.getText()+numeros.getText();
        placa = placa.toUpperCase();
        String tipo = (String) tipoT.getSelectedItem();
        String comb = (String) combustivel.getSelectedItem();
        
        String passando = "cv"+placa+"|"+motorista.getText()+"|"+tipo+"|"+
                senha.getText()+"|"+ano.getText()+"|"+comb;
        
        String vrf = null;
        try {
            conex.enviar(passando);
            vrf = conex.receber();
        } catch (IOException ex) {
            Logger.getLogger(cadastrarVan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(vrf.equals("fasle")){
            JOptionPane.showMessageDialog(null, "TRANSPORTE ["+placa+"]\nJ√Å CADASTRADO");
        } else{
            JOptionPane.showMessageDialog(null, "TRANSPORTE ["+placa+"]\nCADASTRADO COM SUCESSO");
            menuGerente mg = new menuGerente(conex);
            mg.setVisible(true);
            mg.setLocationRelativeTo(null);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

