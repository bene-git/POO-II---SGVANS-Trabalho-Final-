    public void setarExcluir(String tudo){
        
        String [] vet = new String [11];
        String gamb = "";
        int p = 0;
        
        for(int i=0;i<tudo.length();i++){
            gamb = gamb + tudo.substring(i, i+1);
            if(tudo.substring(i, i+1).equals("|")||i==tudo.length()-1){
                vet[p] = gamb;
                gamb = "";
                p++;
            }
        }
        
        letras1.setText(vet[1].substring(0, 3));
        numeros1.setText(vet[1].substring(3, 7));
        
        tipo1.setText(vet[3].substring(0, vet[3].length()-1));
        motorista1.setText(vet[2].substring(0, vet[2].length()-1));
        
        entrada1.setText(vet[9].substring(0, vet[9].length()-1));
        gastos1.setText(vet[10].substring(0, vet[10].length()-1));
        
        double ent = Double.parseDouble(vet[9].substring(0, vet[9].length()-1));
        double sai = Double.parseDouble(vet[10].substring(0, vet[10].length()));
        
        lucro1.setText(String.valueOf(ent-sai));
        
        edmundo.setText(String.valueOf((ent-sai)*0.40));
        valdemir.setText(String.valueOf((ent-sai)*0.60));
        
    }
    
