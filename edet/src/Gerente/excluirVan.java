    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String placa = letras1.getText()+numeros1.getText();
        String passando = "ev"+placa;
        
        String vrf = null;
        
        try {
            conex.enviar(passando);
            vrf = conex.receber();
        } catch (IOException ex) {
            Logger.getLogger(excluirVan.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(vrf.equals("false")){
            JOptionPane.showMessageDialog(null, "COULD NOT DELETE");
        } else{
            JOptionPane.showMessageDialog(null, placa+" DELETE");
            JOptionPane.showMessageDialog(null, "LUCRO:\nEDMUNDO = "+edmundo.getText()+
                "\nVALDEMIR = "+valdemir.getText());
            
            menuGerente mg = new menuGerente(conex);
            mg.setVisible(true);
            mg.setLocationRelativeTo(null);
            dispose();
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

