    public void setar(String p, String m, String a, String g){
        placa.setText(p);
        motorista.setText(m);
        arrecadado.setText(a);
        gasto.setText(g);
        double ar = Double.parseDouble(a);
        double ga = Double.parseDouble(g);
        lucro.setText(String.valueOf(ar-ga));
    }
    
