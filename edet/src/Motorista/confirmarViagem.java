    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String passando = placa.getText()+"|"+arrecadado.getText()+"|"+gasto.getText();
        
        String vrf = null;
        
        try {
            conex.enviar("rv"+passando);
            vrf = conex.receber();
        } catch (IOException ex) {
            Logger.getLogger(confirmarViagem.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(vrf.equals("false")){
            JOptionPane.showMessageDialog(null, "ERRO AO\nREGISTRAR VIAGEM");
        } else {
            JOptionPane.showMessageDialog(null, "VIAGEM CADASTRADA\n"
                    + "ARRECADADO = "+arrecadado.getText()+"\n"
                    + "GASTOS     = "+gasto.getText()+"\n"
                    + "LUCRO      = "+lucro.getText());
        }
        menuMotorista mm = new menuMotorista(conex);
        mm.setar(placa.getText(), motorista.getText());
        mm.setVisible(true);
        mm.setLocationRelativeTo(null);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

